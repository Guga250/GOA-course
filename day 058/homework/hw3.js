// ჩვენ ვისწავლეთ: toUpperCase, toLowerCase, indexOf, trim, trimStart, trimEnd.
// toUpperCase სტრინგის ყველა ასოს ადიდებს.
console.log("heLlo".toUpperCase())
console.log("JavaScript".toUpperCase())
console.log("stupid".toUpperCase())
// toLowerCase სტრინგის ყვლა ასოს აპატარავებს.
console.log("bYeEEEEEE".toLowerCase())
console.log("HAT HAT HAT".toLowerCase())
console.log("OH MY GOOOOD".toLowerCase())
// indexOf ვუთითებთ ასოს და რომელ ასოსაც იპოვის პირველს იმის ინდექს გამოიტანს.
console.log("Tree".indexOf("e"))
console.log("apple".indexOf("p"))
console.log("Feel".indexOf("e"))
// trim აშორებს ზედმეტ ტექსტს სტრინგიდან.
console.log("---------------Sunshine###############".trim())
console.log("---------------five five five five###############".trim())
console.log("---------------Star###############".trim())
// trimStart-საც იგივე დანიშნულება აქვს მაგრამ მარტო დასაწყიში აშორებს.
console.log("---------------Red car###############".trim())
console.log("---------------Guga###############".trim())
console.log("---------------Jajanidze###############".trim())
// trimEnd-საც იგივე დანიშნულება აქვს მაგრამ მარტო დასასრულში აშორებს.
console.log("---------------Blue moon###############".trim())
console.log("---------------Dont belive###############".trim())
console.log("---------------Georgia###############".trim())